-- dictionary for quick search
local dictionary = {}
-- base on sprites
local letter_base = 287
-- Build Dictionary

for i,letter in ipairs(letters) do -- i start from 1 instead of 0
    dictionary[letter] = letter_base + i
end

-- Print String
function printUI:printSpr(input_string,x,y,scale)
    for i=1, #input_string do
        spr(dictionary[string.sub(input_string,i,i)], x + i * 8 * scale, y, -1, scale, 0, 0, 1, 1)
    end
end

return printUI
